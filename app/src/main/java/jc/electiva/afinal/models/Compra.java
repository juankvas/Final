public class Compra {

    String nombre, precio, imagen, promoc;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
    public String getPromoc(){
        return promoc;
    }

    public void setPromoc(String promoc) {
        this.promoc = promoc;
    }






}
